#include "ultimate_libft.h"

/**
 * @brief
 * Calculates the factorial of a number recursively.
 * 
 * @returns
 * The factorial of a number if nb is positive.*/
/**
 * - Else 0.
*/
int	ft_recursive_factorial(int nb)
{
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	return (nb * ft_recursive_factorial(nb - 1));
}

