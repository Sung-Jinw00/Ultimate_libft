#include "ultimate_libft.h"

/**
 * @brief
 * Swap 2 chars.
*/
void	ft_swap_char(char *c1, char *c2)
{
	char	c3;

	c3 = *c1;
	*c1 = *c2;
	*c2 = c3;
}
